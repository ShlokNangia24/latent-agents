"""Benchmark: Non-Latent (text-based) vs Latent multi-agent pipelines.

The core question this file answers:
  Does latent-space communication between agents match (or beat) traditional
  text-based multi-agent communication -- while using fewer tokens and less time?

4-Agent pipeline:
  1. PLANNER  (Plan-and-Solve)    -- plan + acceptance checks
  2. SOLVER   (Self-consistency)  -- 3 candidate solutions, pick best
  3. CRITIC   (Self-Refine)       -- tighten / fix the chosen solution
  4. VERIFIER (Chain-of-Verification) -- verify claims, output final

Pipelines compared:
  1. Single Agent        -- one solver, no collaboration
  2. Non-Latent 4-Agent  -- Planner -> Solver -> Critic -> Verifier, all via text
  3. Latent 4-Agent      -- Planner -> Solver -> Critic latent, Verifier answers

Metrics:
  - Accuracy       (does the output contain the expected answer?)
  - Prompt tokens   (total tokens fed into the model across all agents)
  - Generated tokens (total tokens produced by all agents, including intermediate)
  - Wall-clock time

Run:
    pytest tests/test_benchmark.py -v -s --run-integration
"""

