@dataclass
class RunStats:
    """Aggregate stats for a single question run."""
    prompt_tokens: int = 0       # total prompt tokens across all agents
    generated_tokens: int = 0    # total tokens generated across all agents
    final_gen_tokens: int = 0    # tokens generated by the final (answering) agent only
    intermediate_tokens: int = 0 # tokens generated by non-final agents (text-based only)
    wall_time_s: float = 0.0


def count_tokens(tokenizer, text: str) -> int:
    if not text:
        return 0
    return len(tokenizer.encode(text, add_special_tokens=False))


def check_answer(output: str, bq: BenchmarkQuestion) -> bool:
    low = output.lower()
    if bq.expected.lower() in low:
        return True
    return any(alt.lower() in low for alt in bq.alternatives)


