@pytest.mark.integration
class TestNonLatentVsLatent4Agent:
    """Head-to-head: 4-agent text-based pipeline vs 4-agent latent pipeline."""

    def test_four_agent_comparison(self, model):
        set_seed(42)

        # --- Non-latent 4-agent (text-based) ---
        text_specs = [
            {"name": "Planner",  "prompt_fn": planner_prompt_text, "is_final": False},
            {"name": "Solver",   "prompt_fn": solver_prompt_text,  "is_final": False},
            {"name": "Critic",   "prompt_fn": critic_prompt_text,  "is_final": False},
            {"name": "Verifier", "prompt_fn": verifier_prompt_text, "is_final": True},
        ]

        # --- Latent 4-agent (KV-cache) ---
        latent_agents_list = [
            Agent(name="Planner", role="planner", prompt_fn=latent_planner),
            Agent(name="Solver", role="solver", prompt_fn=latent_solver),
            Agent(name="Critic", role="critic", prompt_fn=latent_critic),
            Agent(name="Verifier", role="verifier", prompt_fn=latent_verifier, is_final=True),
        ]
        latent_pipeline = LatentPipeline(
            model, latent_agents_list, latent_steps=10, max_new_tokens=400, temperature=0.6,
        )

        text_rows = []
        latent_rows = []
        text_correct = 0
        latent_correct = 0
        text_total_stats = RunStats()
        latent_total_stats = RunStats()

        for bq in QUESTIONS:
            # Non-latent run
            set_seed(42)
            t_answer, t_stats = run_text_pipeline(model, bq.question, text_specs)
            t_hit = check_answer(t_answer, bq)
            if t_hit:
                text_correct += 1
            text_total_stats.prompt_tokens += t_stats.prompt_tokens
            text_total_stats.generated_tokens += t_stats.generated_tokens
            text_total_stats.intermediate_tokens += t_stats.intermediate_tokens
            text_total_stats.final_gen_tokens += t_stats.final_gen_tokens
            text_total_stats.wall_time_s += t_stats.wall_time_s

            # Latent run
            set_seed(42)
            l_answer, l_stats = run_latent_pipeline(model, latent_pipeline, bq.question)
            l_hit = check_answer(l_answer, bq)
            if l_hit:
                latent_correct += 1
            latent_total_stats.prompt_tokens += l_stats.prompt_tokens
            latent_total_stats.generated_tokens += l_stats.generated_tokens
            latent_total_stats.intermediate_tokens += l_stats.intermediate_tokens
            latent_total_stats.final_gen_tokens += l_stats.final_gen_tokens
            latent_total_stats.wall_time_s += l_stats.wall_time_s

            text_rows.append({
                "Cat": bq.category[:6],
                "Question": bq.question[:45],
                "Exp": bq.expected,
                "OK": "Y" if t_hit else "N",
                "Prompt": t_stats.prompt_tokens,
                "Inter": t_stats.intermediate_tokens,
                "Final": t_stats.final_gen_tokens,
                "Total Gen": t_stats.generated_tokens,
                "Time": f"{t_stats.wall_time_s:.1f}s",
                "Answer": t_answer[:60].replace("\n", " "),
            })
            latent_rows.append({
                "Cat": bq.category[:6],
                "Question": bq.question[:45],
                "Exp": bq.expected,
                "OK": "Y" if l_hit else "N",
                "Prompt": l_stats.prompt_tokens,
                "Inter": l_stats.intermediate_tokens,
                "Final": l_stats.final_gen_tokens,
                "Total Gen": l_stats.generated_tokens,
                "Time": f"{l_stats.wall_time_s:.1f}s",
                "Answer": l_answer[:60].replace("\n", " "),
            })

        cols = ["Cat", "Question", "Exp", "OK", "Prompt", "Inter", "Final", "Total Gen", "Time", "Answer"]

        print_table("NON-LATENT 4-AGENT (text: Planner->Solver->Critic->Verifier)", text_rows, cols)
        print_table("LATENT 4-AGENT (KV-cache: Plan->Solve->Critic->Verify, steps=10)", latent_rows, cols)

        # --- Summary comparison ---
        n = len(QUESTIONS)
        summary = [
            {
                "Pipeline": "Non-Latent 4-Agent (text)",
                "Accuracy": f"{text_correct}/{n} ({text_correct/n*100:.0f}%)",
                "Prompt Tok": text_total_stats.prompt_tokens,
                "Intermediate Tok": text_total_stats.intermediate_tokens,
                "Final Tok": text_total_stats.final_gen_tokens,
                "Total Gen Tok": text_total_stats.generated_tokens,
                "All Tok": text_total_stats.prompt_tokens + text_total_stats.generated_tokens,
                "Time": f"{text_total_stats.wall_time_s:.1f}s",
            },
            {
                "Pipeline": "Latent 4-Agent (KV-cache)",
                "Accuracy": f"{latent_correct}/{n} ({latent_correct/n*100:.0f}%)",
                "Prompt Tok": latent_total_stats.prompt_tokens,
                "Intermediate Tok": latent_total_stats.intermediate_tokens,
                "Final Tok": latent_total_stats.final_gen_tokens,
                "Total Gen Tok": latent_total_stats.generated_tokens,
                "All Tok": latent_total_stats.prompt_tokens + latent_total_stats.generated_tokens,
                "Time": f"{latent_total_stats.wall_time_s:.1f}s",
            },
        ]
        print_table(
            "4-AGENT COMPARISON: Non-Latent vs Latent",
            summary,
            ["Pipeline", "Accuracy", "Prompt Tok", "Intermediate Tok", "Final Tok",
             "Total Gen Tok", "All Tok", "Time"],
        )

        # Token savings
        if text_total_stats.generated_tokens > 0:
            savings = (1 - latent_total_stats.generated_tokens / text_total_stats.generated_tokens) * 100
            print(f"  Token savings (generated): {savings:.1f}%")
        if text_total_stats.wall_time_s > 0:
            speedup = text_total_stats.wall_time_s / max(latent_total_stats.wall_time_s, 0.001)
            print(f"  Speedup: {speedup:.2f}x\n")

        assert len(text_rows) == n
        assert len(latent_rows) == n


@pytest.mark.integration
class TestFullComparison:
    """Grand summary: single-agent vs non-latent 4-agent vs latent 4-agent."""

    def test_all_pipelines(self, model):
        """Run all 3 pipeline configurations and print a combined table."""
        n = len(QUESTIONS)

        configs: List[Dict[str, Any]] = [
            {
                "name": "1. Single Agent (baseline)",
                "type": "text",
                "specs": [
                    {"name": "Solver", "prompt_fn": solver_prompt_plain, "is_final": True},
                ],
            },
            {
                "name": "2. Non-Latent 4-Agent",
                "type": "text",
                "specs": [
                    {"name": "Planner",  "prompt_fn": planner_prompt_text, "is_final": False},
                    {"name": "Solver",   "prompt_fn": solver_prompt_text,  "is_final": False},
                    {"name": "Critic",   "prompt_fn": critic_prompt_text,  "is_final": False},
                    {"name": "Verifier", "prompt_fn": verifier_prompt_text, "is_final": True},
                ],
            },
            {
                "name": "3. Latent 4-Agent (steps=10)",
                "type": "latent",
                "agents": [
                    Agent(name="Planner", role="planner", prompt_fn=latent_planner),
                    Agent(name="Solver", role="solver", prompt_fn=latent_solver),
                    Agent(name="Critic", role="critic", prompt_fn=latent_critic),
                    Agent(name="Verifier", role="verifier", prompt_fn=latent_verifier, is_final=True),
                ],
                "latent_steps": 10,
            },
        ]

        summary_rows = []

        for config in configs:
            set_seed(42)
            correct = 0
            total = RunStats()
            cat_correct: Dict[str, int] = {}
            cat_total: Dict[str, int] = {}

            if config["type"] == "text":
                for bq in QUESTIONS:
                    answer, stats = run_text_pipeline(model, bq.question, config["specs"])
                    hit = check_answer(answer, bq)
                    if hit:
                        correct += 1
                    total.prompt_tokens += stats.prompt_tokens
                    total.generated_tokens += stats.generated_tokens
                    total.intermediate_tokens += stats.intermediate_tokens
                    total.final_gen_tokens += stats.final_gen_tokens
                    total.wall_time_s += stats.wall_time_s
                    cat_correct[bq.category] = cat_correct.get(bq.category, 0) + (1 if hit else 0)
                    cat_total[bq.category] = cat_total.get(bq.category, 0) + 1
            else:
                pipeline = LatentPipeline(
                    model, config["agents"],
                    latent_steps=config["latent_steps"],
                    max_new_tokens=400, temperature=0.6,
                )
                for bq in QUESTIONS:
                    answer, stats = run_latent_pipeline(model, pipeline, bq.question)
                    hit = check_answer(answer, bq)
                    if hit:
                        correct += 1
                    total.prompt_tokens += stats.prompt_tokens
                    total.generated_tokens += stats.generated_tokens
                    total.intermediate_tokens += stats.intermediate_tokens
                    total.final_gen_tokens += stats.final_gen_tokens
                    total.wall_time_s += stats.wall_time_s
                    cat_correct[bq.category] = cat_correct.get(bq.category, 0) + (1 if hit else 0)
                    cat_total[bq.category] = cat_total.get(bq.category, 0) + 1

            accuracy = correct / n * 100
            cat_str = ", ".join(
                f"{c}: {cat_correct.get(c,0)}/{cat_total.get(c,0)}"
                for c in sorted(cat_total)
            )

            summary_rows.append({
                "Configuration": config["name"],
                "Accuracy": f"{correct}/{n} ({accuracy:.0f}%)",
                "Prompt": total.prompt_tokens,
                "Intermed": total.intermediate_tokens,
                "Final": total.final_gen_tokens,
                "Total Gen": total.generated_tokens,
                "All Tok": total.prompt_tokens + total.generated_tokens,
                "Time": f"{total.wall_time_s:.1f}s",
                "By Category": cat_str,
            })

        print_table(
            "GRAND COMPARISON -- All Pipeline Configurations",
            summary_rows,
            ["Configuration", "Accuracy", "Prompt", "Intermed", "Final",
             "Total Gen", "All Tok", "Time", "By Category"],
        )

        # Token savings: latent vs non-latent 4-agent
        print("  --- Token & Speed Analysis ---")
        t = summary_rows[1]  # Non-Latent 4-Agent
        l = summary_rows[2]  # Latent 4-Agent
        t_gen = t["Total Gen"]
        l_gen = l["Total Gen"]
        if t_gen > 0:
            savings = (1 - l_gen / t_gen) * 100
            print(f"  4-Agent: Latent generates {savings:.0f}% fewer tokens than Non-Latent")
        t_time = float(t["Time"].rstrip("s"))
        l_time = float(l["Time"].rstrip("s"))
        if l_time > 0:
            print(f"  4-Agent: Latent is {t_time/l_time:.1f}x faster than Non-Latent")

        print()
        assert len(summary_rows) == 3
