def solver_prompt_plain(question: str, context: str) -> List[Dict[str, str]]:
    """Single-agent baseline: no prior context."""
    return [
        {"role": "system", "content": (
            "You are an expert problem solver. Think step by step, then give "
            "a clear, concise final answer on the last line."
        )},
        {"role": "user", "content": (
            f"Answer the following question. Show brief reasoning, then state "
            f"the final answer clearly.\n\nQuestion: {question}"
        )},
    ]


# --- Non-latent (text-based) 4-agent pipeline prompts ---

def planner_prompt_text(question: str, context: str) -> List[Dict[str, str]]:
    """Agent 1 – PLANNER (Plan-and-Solve)."""
    return [
        {"role": "system", "content": (
            "You are PLANNER.\n"
            "Create a plan before solving.\n\n"
            "Output only:\n"
            "PLAN: 3-7 numbered steps, each with a concrete deliverable.\n"
            "CHECKS: bullet list of acceptance criteria and how to validate correctness."
        )},
        {"role": "user", "content": f"Task: {question}"},
    ]


def solver_prompt_text(question: str, prior_text: str) -> List[Dict[str, str]]:
    """Agent 2 – SOLVER (Self-consistency, N=3)."""
    return [
        {"role": "system", "content": (
            "You are SOLVER.\n"
            "Use the PLAN. Generate 3 independent solution attempts (different approaches).\n"
            "For each attempt output: Final answer + key steps only.\n"
            "Then select the best using consistency and the CHECKS.\n\n"
            "Output only:\n"
            "ATTEMPT 1:\n"
            "ATTEMPT 2:\n"
            "ATTEMPT 3:\n"
            "CHOSEN ANSWER: (with brief why)"
        )},
        {"role": "user", "content": (
            f"--- PLANNER output ---\n{prior_text}\n--- end ---\n\n"
            f"Task: {question}"
        )},
    ]


def critic_prompt_text(question: str, prior_text: str) -> List[Dict[str, str]]:
    """Agent 3 – CRITIC (Self-Refine)."""
    return [
        {"role": "system", "content": (
            "You are CRITIC.\n"
            "Improve the CHOSEN ANSWER with minimal edits.\n\n"
            "Output only:\n"
            "TOP ISSUES (max 5): specific problems or gaps\n"
            "PATCH: the revised answer (clean, final-draft quality)"
        )},
        {"role": "user", "content": (
            f"--- Previous agents ---\n{prior_text}\n--- end ---\n\n"
            f"Task: {question}"
        )},
    ]


def verifier_prompt_text(question: str, prior_text: str) -> List[Dict[str, str]]:
    """Agent 4 – VERIFIER (CoVe). Final agent."""
    return [
        {"role": "system", "content": (
            "You are VERIFIER using Chain-of-Verification. Treat PATCH as a draft.\n\n"
            "Do these steps strictly and keep them short:\n"
            "1. List claims that might be wrong.\n"
            "2. Write verification questions for those claims.\n"
            "3. Answer them independently (mark unknown if unsure).\n"
            "4. Produce a corrected final response.\n\n"
            "Output only:\n"
            "CLAIMS TO CHECK:\n"
            "VERIFICATION QUESTIONS:\n"
            "INDEPENDENT ANSWERS:\n"
            "FINAL VERIFIED OUTPUT:"
        )},
        {"role": "user", "content": (
            f"--- Previous agents ---\n{prior_text}\n--- end ---\n\n"
            f"Task: {question}"
        )},
    ]


# --- Latent 4-agent pipeline prompts (KV-cache communication) ---

def latent_planner(question: str, context: str) -> List[Dict[str, str]]:
    """Latent Agent 1 – PLANNER."""
    return [
        {"role": "system", "content": (
            "You are PLANNER.\n"
            "Create a plan before solving.\n\n"
            "Think about:\n"
            "PLAN: 3-7 numbered steps, each with a concrete deliverable.\n"
            "CHECKS: acceptance criteria and how to validate correctness."
        )},
        {"role": "user", "content": f"Task: {question}"},
    ]


def latent_solver(question: str, context: str) -> List[Dict[str, str]]:
    """Latent Agent 2 – SOLVER."""
    return [
        {"role": "system", "content": (
            "You are SOLVER. You have received internal reasoning context from PLANNER.\n"
            "Use the plan to consider 3 independent solution approaches.\n"
            "Think about which attempt is most consistent with the acceptance checks.\n"
            "Settle on the best candidate answer."
        )},
        {"role": "user", "content": f"Task: {question}"},
    ]


def latent_critic(question: str, context: str) -> List[Dict[str, str]]:
    """Latent Agent 3 – CRITIC."""
    return [
        {"role": "system", "content": (
            "You are CRITIC. You have received internal reasoning context from "
            "PLANNER and SOLVER.\n"
            "Identify up to 5 specific problems or gaps in the chosen answer.\n"
            "Think about how to patch and improve it to final-draft quality."
        )},
        {"role": "user", "content": f"Task: {question}"},
    ]


def latent_verifier(question: str, context: str) -> List[Dict[str, str]]:
    """Latent Agent 4 – VERIFIER (final). Generates visible text."""
    return [
        {"role": "system", "content": (
            "You are VERIFIER using Chain-of-Verification. You have received "
            "internal reasoning context from PLANNER, SOLVER, and CRITIC.\n\n"
            "Do these steps strictly and keep them short:\n"
            "1. List claims that might be wrong.\n"
            "2. Write verification questions for those claims.\n"
            "3. Answer them independently (mark unknown if unsure).\n"
            "4. Produce a corrected final response.\n\n"
            "Output only:\n"
            "CLAIMS TO CHECK:\n"
            "VERIFICATION QUESTIONS:\n"
            "INDEPENDENT ANSWERS:\n"
            "FINAL VERIFIED OUTPUT:"
        )},
        {"role": "user", "content": f"Task: {question}"},
    ]


# ---------------------------------------------------------------------------
# Non-latent (text-based) multi-agent pipeline runner
# ---------------------------------------------------------------------------

@torch.no_grad()
def run_text_pipeline(
    model: LatentModel,
    question: str,
    agent_specs: List[Dict[str, Any]],
    max_new_tokens_intermediate: int = 300,
    max_new_tokens_final: int = 400,
    temperature: float = 0.6,
    top_p: float = 0.95,
) -> Tuple[str, RunStats]:
    """Run a traditional text-based multi-agent pipeline.

    Each agent generates text. Non-final agents' text is passed as context
    to the next agent's prompt. This is the "slow" approach that latent
    communication is designed to replace.

    Parameters
    ----------
    agent_specs : list of dicts
        Each dict has:
          - "prompt_fn": callable(question, prior_text) -> messages
          - "is_final": bool
    """
    stats = RunStats()
    accumulated_text = ""

    for spec in agent_specs:
        prompt_fn = spec["prompt_fn"]
        is_final = spec["is_final"]
        max_tok = max_new_tokens_final if is_final else max_new_tokens_intermediate

        messages = prompt_fn(question, accumulated_text)
        prompts, input_ids, attention_mask = model.prepare_chat_batch(
            [messages], add_generation_prompt=True,
        )
        stats.prompt_tokens += int(attention_mask.sum().item())

        t0 = time.perf_counter()
        texts, _ = model.generate_text_batch(
            input_ids, attention_mask,
            max_new_tokens=max_tok, temperature=temperature, top_p=top_p,
        )
        elapsed = time.perf_counter() - t0
        stats.wall_time_s += elapsed

        output = texts[0].strip()
        gen_tok = count_tokens(model.tokenizer, output)
        stats.generated_tokens += gen_tok

        if is_final:
            stats.final_gen_tokens = gen_tok
        else:
            stats.intermediate_tokens += gen_tok
            # Accumulate text for the next agent
            agent_name = spec.get("name", "Agent")
            accumulated_text += f"--- {agent_name} output ---\n{output}\n"

    return output, stats


# ---------------------------------------------------------------------------
# Latent pipeline runner
# ---------------------------------------------------------------------------

def run_latent_pipeline(
    model: LatentModel,
    pipeline: LatentPipeline,
    question: str,
) -> Tuple[str, RunStats]:
    """Run the latent pipeline, collecting stats."""
    stats = RunStats()

    # Count prompt tokens across all agents
    for agent in pipeline.agents:
        msgs = agent.prompt_fn(question, "")
        rendered = model.render_chat(msgs, add_generation_prompt=True)
        stats.prompt_tokens += count_tokens(model.tokenizer, rendered)

    t0 = time.perf_counter()
    result = pipeline.run(question)
    stats.wall_time_s = time.perf_counter() - t0

    stats.generated_tokens = count_tokens(model.tokenizer, result.text)
    stats.final_gen_tokens = stats.generated_tokens
    stats.intermediate_tokens = 0  # latent agents produce zero text tokens

    return result.text, stats


# ---------------------------------------------------------------------------
# Pretty printing
# ---------------------------------------------------------------------------

def print_table(title: str, rows: List[Dict[str, Any]], columns: List[str]) -> None:
    widths = {col: len(col) for col in columns}
    for row in rows:
        for col in columns:
            widths[col] = max(widths[col], len(str(row.get(col, ""))))

    total_w = sum(widths.values()) + 3 * (len(columns) - 1)
    header = " | ".join(col.ljust(widths[col]) for col in columns)
    sep = "-+-".join("-" * widths[col] for col in columns)

    print(f"\n{'=' * total_w}")
    print(f"  {title}")
    print(f"{'=' * total_w}")
    print(header)
    print(sep)
    for row in rows:
        print(" | ".join(str(row.get(col, "")).ljust(widths[col]) for col in columns))
    print()


